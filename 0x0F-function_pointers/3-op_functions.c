#include "3-calc.h"

/**
  * op_add - Do a + b
  * @a: integer
  * @b: integer
  *
  * Return: a + b
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - Do a - b
  * @a: integer
  * @b: integer
  *
  * Return: a - b
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - Do a * b
  * @a: integer
  * @b: integer
  *
  * Return: a * b
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - Do a / b
  * @a: integer
  * @b: integer
  *
  * Return: a / b
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - Do a % b
  * @a: integer
  * @b: integer
  *
  * Return: a + b
  */

int op_mod(int a, int b)
{
	return (a % b);
}
